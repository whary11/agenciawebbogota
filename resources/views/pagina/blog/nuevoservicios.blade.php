                            <div class="dlab-post-text text-justify">
                            <p><b>Conozca las nuevas herramientas y funcionalidades que en Agencia Web Bogotá estamos en capacidad de integrar en su sitio web para potenciar aún más su presencia en Internet, aumentar su efectividad y mejorar sus ingresos.</b></p>
                            <picture>
                            <source srcset="{{asset('images/blog/funciones-sitio-web.webp')}}" type="image/webp">
                                <img src="{{asset('images/blog/funciones-sitio-web.jpg')}}" alt="Hombre de negocios usando dispositivos de tecnología e íconos de interfaz">
                            </picture>
                            <div class="p-t30"></div>
                            <p>Agencia Web Bogotá lleva poco más de 2 años en la ejecución de proyectos digitales para diferentes segmentos del mercado, sabemos que es un corto tiempo de existencia, pero vamos avanzando a pasos agigantados.</p>
                            <p>El mayor de los crecimientos se ha dado en el campo cognitivo, con cada desarrollo aprendemos a sacar mayor provecho de las tecnologías disponibles tanto en diseño como en programación, pudiendo así ofrecer a nuestros clientes cada vez más y mejores productos digitales sumados a un buen servicio.</p>
                            <p>Las siguientes son algunas de las nuevas características que <b>hemos implementado con rotundo éxito</b> en diferentes sitios web y que hoy nos complace poner a disposición de todos nuestros clientes para aumentar la funcionalidad, el alcance y el posicionamiento de sus sitios corporativos. Veamos:</p>
                            <h2 class="texto-blog text-pink">Aumento de velocidad de carga</h2>
                            <p>Haciendo uso de herramientas como plugins de caché avanzados en el caso de WordPress, minificación (compresión) de código fuente y uso de nuevos formatos de imagen como webp; hemos podido optimizar la velocidad de carga de los sitios web hasta en un 300%.</p>
                            <p>Orgullosamente, hemos logrado sitios web con un puntaje de 98/100 en <i>PageSpeed Insights</i> de Google y una velocidad de carga promedio de 1.5 segundos según la herramienta <i>Think With Google</i>.</p>
                            <h2 class="texto-blog text-pink">Integración con Pasarelas de Pago</h2>
                            <p>Integrar los pagos online en un sitio web ya no es una tarea eximia de programación. Ahora es muy <b>fácil y económico</b> contar con la posibilidad de recibir pagos de clientes con diversos medios de pago.</p>
                            <p>En Agencia Web Bogotá hemos simplificado un montón los procesos de integración de pagos online para hacerlo más accesible a nuestros clientes. En pocos minutos y algunos clics las principales plataformas de pago estarán integradas a su sitio web.</p>
                            <h2 class="texto-blog text-pink">Acceso y registro de usuarios Personalizado en WordPress</h2>
                            <p>Hemos logrado modificar al 100% la página de registro e ingreso de usuarios de WordPress. Esto es genial porque no se perderá en ningún momento la identidad del sitio al registrarse o acceder a una cuenta. Además, optimizamos el procesos para registro y aprobación de usuarios, zonas con contenido exclusivo y notificaciones personalizadas por correo electrónico.</p>
                            <h2 class="texto-blog text-pink">Espectaculares Galerías de Imágenes</h2>
                            <p>En algunos sitios, la parte visual juega un papel muy importante. Es por eso que hemos estado implementando un <b>nuevo plugin de WordPress</b> que permite tener galerías de imágenes realmente increíbles. Con efectos visuales muy llamativos, sin afectar la velocidad de carga del sitio. De fácil administración para agregar, modificar o crear nuevas galerías.</p>    
                            <h2 class="texto-blog text-pink">Proyectos en Laravel</h2>
                            <p>Nos hemos vuelto fanáticos de Laravel, se trata de un sistema que permite crear, organizar y programar cualquier aplicación o página web.
                            Ya no desarrollamos páginas web en HTML usando el método convencional de archivos individuales, <b>ahora todo lo hacemos por Laravel</b>. Esto permite tener mayor velocidad en la ejecución de las aplicaciones, mejor entendimiento del código, reducir los tiempos de entrega a cliente final, aumentar la seguridad de los proyectos e incluir de forma muy fácil cualquier característica adicional que el cliente requiera.</p>
                            <p>Los proyectos desarrollados en Laravel son totalmente escalables y al tratarse de una librería estándar, cualquier desarrollador comprenderá el código para futuras modificaciones.</p>
                            <h2 class="texto-blog text-pink">Copias de seguridad de los sitios web en Github</h2>
                            <p>¿Será importante conservar copias de seguridad de los sitios de los clientes? <b>Es monumentalmente importante</b>. Por eso usamos el gestor de control de versiones más seguro que existe, Github. Allí estamos almacenando cada proyecto desarrollado para evitar lágrimas cuando algo imprevisto ocurra.</p>
                            <h2 class="texto-blog text-pink">Migración a VPS</h2>
                            <p>Los servidores compartidos tienen los días contados, es por eso que cada vez más estamos trabajando con servidores privados virtuales VPS para alojar las páginas de nuestros clientes. Teniendo en cuenta también el <a class="text-blue" href="{{url('/blog/alarmante-aumento-en-los-costos-de-alojamiento-de-paginas-web')}}">alarmante aumento de costos de las licencias de cPanel</a> ¡Una locura!</p>
                            <h2 class="texto-blog">Y seguimos adelante…</h2>
                            <p>Muy pronto publicaremos la segunda parte de esta lista de nuevas características disponibles para nuestros clientes. Cada día descubrimos nuevas cosas, nuevos métodos, crecemos con cada proyecto y estamos trabajando por mejorar el posicionamiento orgánico en Google de los sitios de nuestros clientes.</p>
                            <p>Si desea incluir alguna de las anteriores funcionalidades en su sitio web no dude en hacernolo saber. Estaremos gustosos de ofrecer beneficios especiales a nuestros clientes que continúan confiando en Agencia Web Bogotá. </p>
