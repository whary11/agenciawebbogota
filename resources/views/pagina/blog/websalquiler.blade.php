                            <h2 class="texto-blog text-red">El Problema</h2>
                            <p>Más del 80% de los sitios web que existen se encuentran inactivos, desperdiciando todo su potencial. Esto se debe a que los propietarios no cuentan con el tiempo y el conocimiento necesario para hacer un uso eficiente de las herramientas disponibles. Entonces se conforman con tener un espacio en Internet a medias que les produce poco o ningún beneficio.</p>
                            <h2 class="texto-blog text-green">La Solución: Sitios web y Tiendas Online en alquiler</h2>
                            <p><b>Así es, en alquiler</b>. Hagamos de cuenta que rentas un departamento para vivir, pero además de pagar por estar allí, el precio te incluye la mudanza, tu alimentación diaria, el lavado de tu ropa, los servicios públicos, conductor personal y todo lo que puedas necesitar. Eso mismo haremos nosotros de hoy en adelante con tu espacio en Internet.</p>
                            <p>Diseñamos de forma profesional y de acuerdo a tus indicaciones, posicionamos en Google tu sitio, lo mantenemos actualizado, renovamos el contenido; en el caso de las tiendas online, administraremos eficientemente tu inventario, te notificaremos de las ventas y mantendremos siempre el contenido fresco para tus clientes.</p>
                            <p>Todo esto por un valor mensual que a la verdad es ¡ridículamente bajo!</p>
                            <h2 class="texto-blog">¿Que beneficios obtienes por tener tu sitio en alquiler?</h2>
                            <p><span class="text-blue">Lo primero es que no tienes absolutamente nada que perder</span>, no debes pagar anticipos, ni firmar contratos; simplemente nos das <span class="text-green">"luz verde"</span> y procederemos con el desarrollo de tu sitio. <b>Pagarás tu primera mensualidad cuando el sitio esté 100% terminado y online.</b></p>
                            <p>Lo segundo es que <b>nosotros generamos las ventas, las visitas y los prospectos por ti</b>. Sabemos como hacerlo, sacaremos todo el provecho posible a tu sitio web y lo haremos a tu favor.</p>
                            <p>Otro beneficio es que siempre habrá profesionales al frente de tu preciado espacio online. Es como tener un departamento de webmasters dedicado a la consecución de clientes.</p>
                            <p>Y si todo no sale como esperabas, cancelas el servicio cuando quieras. Si ves que no estamos haciendo un buen trabajo y que no estás recibiendo beneficios reales por tener tu sitio web, simplemente dejas de pagar tu mensualidad y el sitio se desactivará. Y claro, seguiremos siendo amigos…</p>
                            <h2 class="texto-blog">¿Que obtenemos nosotros de todo esto?</h2>
                            <p>Nuestro principal objetivo desde nuestra fundación ha sido estar cerca de nuestros clientes, que puedan contar con nosotros en el momento que nos necesiten. Por ello hemos ideado esta modalidad que nos permite estar más cerca de nuestros clientes al mismo tiempo que nos exigimos al máximo para dar <b>resultados reales y asegurar un verdadero beneficio</b>.</p>
                            <h2 class="texto-blog">¿Hay alguien más haciendo lo mismo?</h2>
                            <p>Por el momento no, somos pioneros en esta metodología. Y creemos que no es porque a no se le haya ocurrido a nadie, con seguridad si. Pero nuestros colegas webmasters no trabajan bajo este modelo porque supone riesgo de perder tiempo y dinero, pero nosotros hemos decidido jugárnosla por el crecimiento de nuestros clientes.</p>
                            <p>Creemos en nuestro trabajo, creemos en las capacidades de nuestro equipo y creemos en el potencial que tienen los sitios web y tiendas online para escalar un negocio, sea el que sea.</p>
